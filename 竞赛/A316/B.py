from typing import List


class Solution:
    def subarrayGCD(self, nums: List[int], k: int) -> int:
        ans = 0
        for idx, num in enumerate(nums):
            tmp = num
            for i in range(idx, len(nums)):
                tmp = self.gcd(tmp, nums[i])
                if tmp == k:
                    ans += 1
                elif tmp < k:
                    break
        return ans

    def gcd(self, p, q):
        while p != q:
            if p > q:
                p = p-q
            else:
                q = q - p
        return q



